#!/usr/bin/env python3
""" 
contains a type-annotation function of float using 'add'

"""

def add(a: float, b: float) -> float:
    """
    
    Add two floats

    Args:
        a (float):first float
        b (float): second float
        
    Return:
        float: sum of args
    
    """
    
    return a+b

